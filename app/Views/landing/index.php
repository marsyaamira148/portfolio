    function highlightStars(value) {
        stars.forEach(st => {
            if (st.dataset.value <= value) st.classList.add("hovered");
        });
    }

    function resetHover() {
        stars.forEach(st => st.classList.remove("hovered"));
    }

    function setActiveStars(value) {
        stars.forEach(st => {
            st.classList.remove("active");
            if (st.dataset.value <= value) st.classList.add("active");
        });
    }
});
